# Creating a list
my_list = [1, 2, 3, 4, 5]

# Accessing elements
print("First element:", my_list[0])
print("Last element:", my_list[-1])

# Modifying elements
my_list[2] = 10
print("Modified list:", my_list)

# Appending and removing elements
my_list.append(0.5) #it will append the 100 in list in the last [-1] 
my_list.remove(1)  # it will remove 1 from list 
print("Updated list:", my_list)
