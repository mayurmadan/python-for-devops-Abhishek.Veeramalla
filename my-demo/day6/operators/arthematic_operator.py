a = 10
b = 5

#addition
print(a+b)

#Subtraction
print(a-b)

#Multiplication
print(a*b)

#Division
print(a/b)

#Floor Division (//)
print(a//b)

#Modulus (%): Returns the remainder of the division of the left operand by the right operand.[baki]
print(a%b)

#Exponentiation ():** Raises the left operand to the power of the right operand.
print(a**b)
